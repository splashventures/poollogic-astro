function slugToTitle(slug: string): string {
  return slug
    .split('-')
    .map((word) => {
      // Keep small words lowercase unless first word
      const smallWords = ['a', 'an', 'the', 'and', 'or', 'for', 'in', 'on', 'of', 'to', 'vs', 'its', 'your', 'you'];
      if (smallWords.includes(word)) return word;
      if (word === 'diy') return 'DIY';
      if (word === 'faqs') return 'FAQs';
      return word.charAt(0).toUpperCase() + word.slice(1);
    })
    .join(' ')
    // Capitalize first letter regardless
    .replace(/^./, (c) => c.toUpperCase());
}

